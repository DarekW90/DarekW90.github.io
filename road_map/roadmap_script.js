const openPopupButton1 = document.getElementById('open-popup-1');
const closePopupButton1 = document.getElementById('close-popup-1');
const popup1 = document.querySelector('.popup-1');

openPopupButton1.addEventListener('click', () => {
  popup1.style.display = 'block';
});

closePopupButton1.addEventListener('click', () => {
  popup1.style.display = 'none';
});

